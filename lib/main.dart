class SplashToAuth extends StatefulWidget {
  const SplashToAuth({super.key});

  @override
  State<SplashToAuth> createState() => _SplashToAuthState();
}

class _SplashToAuthState extends State<SplashToAuth> {
  bool _showAuthWrapper = false;

  @override
  void initState() {
    super.initState();
    Future.delayed(const Duration(seconds: 2), () {
      setState(() {
        _showAuthWrapper = true;
      });
    });
  }

  @override
  Widget build(BuildContext context) {
    return _showAuthWrapper ? const AuthWrapper() : const SplashScreen();
  }
}

class AuthWrapper extends StatefulWidget {
  const AuthWrapper({super.key});

  @override
  State<AuthWrapper> createState() => _AuthWrapperState();
}

class _AuthWrapperState extends State<AuthWrapper> {
  bool _authChecked = false;
  bool _biometricChecked = false;
  bool _biometricPassed = false;
  bool _autoLoginAttempted = false;

  @override
  void initState() {
    super.initState();
    _initializeAuth();
  }

  Future<void> _initializeAuth() async {
    try {
      print('ğŸ” Initializing auto-login with biometric authentication...');
      
      // Check if user has enabled biometric authentication
      final prefs = await SharedPreferences.getInstance();
      final bioEnabled = prefs.getBool('biometric_enabled') ?? false;
      final autoLoginEnabled = prefs.getBool('auto_login_enabled') ?? true; // Default to true
      
      print('ğŸ” Biometric enabled: $bioEnabled');
      print('ğŸ” Auto-login enabled: $autoLoginEnabled');

      if (autoLoginEnabled) {
        // Try to get stored tokens
        final accessToken = await TokenStorage.getAccessToken();
        final refreshToken = await TokenStorage.getRefreshToken();
        
        print('ğŸ” Stored access token: ${accessToken != null ? 'Present' : 'Not present'}');
        print('ğŸ” Stored refresh token: ${refreshToken != null ? 'Present' : 'Not present'}');

        if (accessToken != null && refreshToken != null) {
          print('ğŸ” Tokens found, attempting auto-login...');
          
          // Try to validate the token by calling /api/user/me
          try {
            final apiService = ApiService();
            final userResponse = await apiService.get('api/user/me');
            
            if (userResponse.statusCode == 200 && userResponse.data != null) {
              print('ğŸ” âœ… Auto-login successful - User validated via /api/user/me');
              
              // Update auth provider with user data
              final authProvider = Provider.of<AuthProvider>(context, listen: false);
              await authProvider.setUserData(userResponse.data);
              
              // If biometric is enabled, check biometric authentication
              if (bioEnabled) {
                print('ğŸ” Biometric enabled, checking biometric authentication...');
                await _checkBiometrics(authProvider);
              } else {
                print('ğŸ” Biometric disabled, proceeding to dashboard...');
                setState(() {
                  _biometricPassed = true;
                  _authChecked = true;
                });
              }
            } else {
              print('ğŸ” âŒ Auto-login failed - Invalid token response');
              await _handleAutoLoginFailure();
            }
          } catch (e) {
            print('ğŸ” âŒ Auto-login failed - API error: $e');
            await _handleAutoLoginFailure();
          }
        } else {
          print('ğŸ” No stored tokens found, showing login page');
          await _handleAutoLoginFailure();
        }
      } else {
        print('ğŸ” Auto-login disabled, showing login page');
        await _handleAutoLoginFailure();
      }
    } catch (e) {
      print('ğŸ” Error during auto-login initialization: $e');
      await _handleAutoLoginFailure();
    }
  }

  Future<void> _handleAutoLoginFailure() async {
    print('ğŸ” Handling auto-login failure...');
    
    // Clear any invalid tokens
    await TokenStorage.clearAllTokens();
    
    // Reset auth provider
    final authProvider = Provider.of<AuthProvider>(context, listen: false);
    await authProvider.logout();
    
    setState(() {
      _authChecked = true;
      _biometricPassed = false;
    });
  }

  Future<void> _checkBiometrics(AuthProvider authProvider) async {
    if (!_biometricChecked && authProvider.isLoggedIn) {
      try {
        print('ğŸ” Checking biometric availability...');
        final isAvailable = await BiometricService.isBiometricAvailable();
        
        if (isAvailable) {
          print('ğŸ” Biometric available, requesting authentication...');
          _biometricChecked = true;
          
          final passed = await BiometricService.authenticateWithBiometrics();
          print('ğŸ” Biometric authentication result: $passed');
          
          if (!passed) {
            print('ğŸ” Biometric authentication failed, logging out...');
            await authProvider.logout();
            await TokenStorage.clearAllTokens();
          }
          
          setState(() {
            _biometricPassed = passed;
            _authChecked = true;
          });
        } else {
          print('ğŸ” Biometric not available, proceeding to dashboard...');
          setState(() {
            _biometricChecked = true;
            _biometricPassed = true;
            _authChecked = true;
          });
        }
      } catch (e) {
        print('ğŸ” Biometric check error: $e');
        setState(() {
          _biometricChecked = true;
          _biometricPassed = true;
          _authChecked = true;
        });
      }
    }
  }

  @override
  Widget build(BuildContext context) {
    return Consumer<AuthProvider>(
      builder: (context, authProvider, child) {
        print(
          'ğŸ” AuthWrapper build - authChecked: $_authChecked, isLoading: ${authProvider.isLoading}, isLoggedIn: ${authProvider.isLoggedIn}, biometricPassed: $_biometricPassed',
        );

        if (!_authChecked || authProvider.isLoading) {
          return const SplashScreen();
        }

        if (authProvider.isLoggedIn && _biometricPassed) {
          print('ğŸ” User authenticated and biometric passed, navigating to dashboard');
          return const DashboardPage();
        }

        print('ğŸ” User not authenticated or biometric failed, showing login page');
        return const LoginPage();
      },
    );
  }
}
